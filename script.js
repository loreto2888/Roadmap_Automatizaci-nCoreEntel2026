function formatClock(date) {
  return date.toLocaleTimeString("es-ES", { hour12: false });
}

function parseDate(value) {
  if (!value || typeof value !== "string") return null;
  const parts = value.split("-");
  if (parts.length !== 3) return null;
  const [day, month, year] = parts.map(Number);
  if (!day || !month || !year) return null;
  return new Date(year, month - 1, day, 0, 0, 0, 0);
}

function formatShortDate(date) {
  return date.toLocaleDateString("es-CL", { day: "2-digit", month: "2-digit" });
}

function formatLongDate(date) {
  return date.toLocaleDateString("es-CL", { day: "2-digit", month: "2-digit", year: "numeric" });
}

function priorityWeight(priority) {
  const normalized = String(priority || "").toLowerCase();
  if (normalized.includes("urgent")) return 3;
  if (normalized.includes("important")) return 2;
  return 1;
}

function taskWindow(task) {
  const start = parseDate(task.start) || new Date();
  const endFromData = parseDate(task.pending);
  const end = endFromData || new Date(start.getTime() + (task.completed ? 3 : 10) * 86400000);
  if (end <= start) {
    return {
      start,
      end: new Date(start.getTime() + 86400000),
    };
  }
  return { start, end };
}

function projectWindow() {
  const windows = getAllTasks().map((task) => taskWindow(task));
  if (!windows.length) {
    const today = new Date();
    return { start: today, end: today, totalDays: 1 };
  }

  const minStart = windows.reduce((acc, item) => (item.start < acc ? item.start : acc), windows[0].start);
  const maxEnd = windows.reduce((acc, item) => (item.end > acc ? item.end : acc), windows[0].end);
  const totalDays = Math.max(1, Math.ceil((maxEnd.getTime() - minStart.getTime()) / 86400000) + 1);

  return { start: minStart, end: maxEnd, totalDays };
}

function updateDateAndCounter() {
  const now = new Date();
  if (currentDate) {
    currentDate.textContent = formatLongDate(now);
  }

  if (!projectDayCount) return;

  const window = projectWindow();
  const dayNumber = Math.max(1, Math.floor((now.getTime() - window.start.getTime()) / 86400000) + 1);
  const clampedDay = Math.min(window.totalDays, dayNumber);
  projectDayCount.textContent = `D${clampedDay}/${window.totalDays}`;
}

function taskDelayInfo(task) {
  if (task.completed) {
    return { state: "on-track", label: "Cerrada" };
  }

  const window = taskWindow(task);
  const today = new Date();
  today.setHours(0, 0, 0, 0);

  const daysLeft = Math.floor((window.end.getTime() - today.getTime()) / 86400000);
  if (daysLeft < 0) {
    return { state: "overdue", label: `Atrasada ${Math.abs(daysLeft)}d` };
  }
  if (daysLeft <= 3) {
    return { state: "due-soon", label: `Vence en ${daysLeft}d` };
  }
  return { state: "on-track", label: `En plazo ${daysLeft}d` };
}

function playAlertTone() {
  try {
    const AudioCtx = window.AudioContext || window.webkitAudioContext;
    if (!AudioCtx) return;
    const context = new AudioCtx();
    const oscillator = context.createOscillator();
    const gain = context.createGain();
    oscillator.type = "triangle";
    oscillator.frequency.value = 740;
    gain.gain.value = 0.0001;
    oscillator.connect(gain);
    gain.connect(context.destination);
    oscillator.start();
    gain.gain.exponentialRampToValueAtTime(0.08, context.currentTime + 0.03);
    gain.gain.exponentialRampToValueAtTime(0.0001, context.currentTime + 0.25);
    oscillator.stop(context.currentTime + 0.26);
  } catch {
    // Audio may be blocked until user interaction.
  }
}

function pushToast(task, toneType) {
  if (!toastStack) return;

  const toast = document.createElement("article");
  toast.className = `toast ${toneType}`;
  const title = document.createElement("strong");
  title.textContent = `Alerta ${task.priority}: ${task.id}`;
  const body = document.createElement("span");
  body.textContent = `${task.title} · ${task.owner}`;
  toast.append(title, body);
  toastStack.prepend(toast);

  while (toastStack.children.length > 4) {
    toastStack.removeChild(toastStack.lastElementChild);
  }

  window.setTimeout(() => {
    toast.remove();
  }, 5200);
}

