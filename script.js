let activeFilter = "all";

const allTasks = roadmap.flatMap((lane) =>
  lane.tasks.map((task) => ({
    ...task,
    laneKey: lane.key,
    laneTitle: lane.title,
    scopeKey: scopeClass(task.scope).replace("scope-", ""),
  }))
);

const scopeLegend = [
  { key: "entel", label: "Entel" },
  { key: "intellicore", label: "Intellicore" },
  { key: "splunk", label: "Splunk" },
  { key: "conjunta", label: "Conjunta" },
];

function formatClock(date) {
  return date.toLocaleTimeString("es-ES", { hour12: false });
}

function badgeClass(priority) {
  const normalized = priority.toLowerCase();
  if (normalized.includes("urgent")) return "urgent";
  if (normalized.includes("important")) return "important";
  return "medium";
}

function scopeClass(scope) {
  const normalized = (scope || "").toLowerCase();
  if (normalized.includes("entel")) return "scope-entel";
  if (normalized.includes("intellicore")) return "scope-intellicore";
  if (normalized.includes("splunk")) return "scope-splunk";
  if (normalized.includes("conjunta") || normalized.includes("conjunto")) return "scope-conjunta";
  return "scope-entel";
}

function matchesFilter(task, filter) {
  if (filter === "all") return true;
  return task.laneKey === filter || task.scopeKey === filter;
}

function persistRoadmap() {
  try {
    localStorage.setItem("roadmapData", JSON.stringify(roadmap));
    localStorage.setItem("roadmapUpdatedAt", new Date().toISOString());
  } catch {
    // Local storage may be unavailable in strict environments.
  }
}

function getVisibleTasks() {
  return allTasks.filter((task) => matchesFilter(task, activeFilter));
}

function ensureScopeBars() {
  if (!scopeBars || scopeBars.children.length > 0) return;

  scopeLegend.forEach((scope) => {
    const row = document.createElement("div");
    row.className = "scope-bar";
    row.dataset.scopeKey = scope.key;

    const head = document.createElement("div");
    head.className = "scope-bar-head";

    const name = document.createElement("span");
    name.textContent = scope.label;

    const value = document.createElement("strong");
    value.className = "scope-bar-value";
    value.textContent = "0";

    head.append(name, value);

    const track = document.createElement("div");
    track.className = "scope-bar-track";

    const fill = document.createElement("div");
    fill.className = `scope-bar-fill scope-${scope.key}`;
    track.appendChild(fill);

    row.append(head, track);
    scopeBars.appendChild(row);
  });
}

function updateCharts(visible, percent) {
  if (statusDonut) {
    const degrees = Math.round((percent / 100) * 360);
    statusDonut.style.background = `conic-gradient(var(--accent-2) ${degrees}deg, rgba(255, 255, 255, 0.1) ${degrees}deg 360deg)`;
  }

  if (statusChartLabel) {
    statusChartLabel.textContent = `${percent}% cierre`;
  }

  const counts = {
    entel: 0,
    intellicore: 0,
    splunk: 0,
    conjunta: 0,
  };

  visible.forEach((task) => {
    if (counts[task.scopeKey] !== undefined) counts[task.scopeKey] += 1;
  });

  const maxCount = Math.max(1, ...Object.values(counts));
  scopeLegend.forEach((scope) => {
    const row = scopeBars?.querySelector(`.scope-bar[data-scope-key="${scope.key}"]`);
    if (!row) return;

    const count = counts[scope.key];
    const width = Math.round((count / maxCount) * 100);
    const value = row.querySelector(".scope-bar-value");
    const fill = row.querySelector(".scope-bar-fill");

    if (value) value.textContent = String(count);
    if (fill) fill.style.width = `${width}%`;
  });

  if (scopeChartLabel) {
    scopeChartLabel.textContent = `${visible.length} tareas`;
  }
}

function updateStats() {
  const visible = getVisibleTasks();
  const closed = visible.filter((task) => task.completed).length;
  const total = visible.length || 1;
  const percent = Math.round((closed / total) * 100);

  overallPercent.textContent = `${percent}%`;
  closedCount.textContent = `${closed}/${visible.length}`;
  timelineLabel.textContent = `${percent}% completado`;
  lastUpdated.textContent = formatClock(new Date());

  const active = visible.filter((task) => !task.completed && task.deposit !== "Cierre").length;
  const inProgress = visible.filter((task) => task.status === "En curso").length;
  const closing = visible.filter((task) => task.deposit === "Cierre" || task.completed).length;

  activeCount.textContent = String(active);
  inProgressCount.textContent = String(inProgress);
  closingCount.textContent = String(closing);

  timelineFill.style.height = `${percent}%`;
  timelineNode.style.setProperty("--node-y", `${Math.max(0, Math.min(500, 520 - (percent / 100) * 520))}px`);
  updateCharts(visible, percent);
}

function renderRoadmap() {
  board.innerHTML = "";

  roadmap.forEach((lane) => {
    const laneNode = laneTemplate.content.cloneNode(true);
    const section = laneNode.querySelector(".lane");
    const kicker = laneNode.querySelector(".lane-kicker");
    const title = laneNode.querySelector("h2");
    const progress = laneNode.querySelector(".lane-progress");
    const total = laneNode.querySelector(".lane-total");
    const track = laneNode.querySelector(".lane-track");

    section.dataset.laneKey = lane.key;
    kicker.textContent = lane.kicker;
    title.textContent = lane.title;

    const laneTasks = lane.tasks.map((task) => ({ ...task, laneKey: lane.key }));
    const closed = laneTasks.filter((task) => task.completed).length;

    progress.textContent = `${closed}/${laneTasks.length} cerradas`;
    total.textContent = `${laneTasks.length} tareas`;

    laneTasks.forEach((task) => {
      const taskNode = taskTemplate.content.cloneNode(true);
      const card = taskNode.querySelector(".task-card");
      const check = taskNode.querySelector(".task-check");
      const id = taskNode.querySelector(".task-id");
      const status = taskNode.querySelector(".task-status");
      const taskTitle = taskNode.querySelector(".task-title");
      const meta = taskNode.querySelector(".task-meta");
      const owner = taskNode.querySelector(".badge-owner");
      const priority = taskNode.querySelector(".badge-priority");
      const scope = taskNode.querySelector(".badge-scope");
      const scopeKey = scopeClass(task.scope);

      card.dataset.laneKey = lane.key;
      card.dataset.taskId = task.id;
      card.dataset.scopeKey = scopeKey.replace("scope-", "");
      card.classList.toggle("completed", task.completed);
      card.classList.add(scopeKey);
      check.setAttribute("aria-pressed", String(task.completed));

      id.textContent = task.id;
      status.textContent = task.completed ? "Cerrada" : task.deposit || task.status;
      taskTitle.textContent = task.title;
      meta.textContent = [task.owner, task.start ? `Inicio ${task.start}` : null, task.pending ? `Pendiente ${task.pending}` : null]
        .filter(Boolean)
        .join(" · ");
      owner.textContent = task.owner;
      priority.textContent = task.priority;
      priority.classList.add(badgeClass(task.priority));
      scope.textContent = task.scope;
      scope.classList.add(scopeKey);

      check.addEventListener("click", () => {
        task.completed = !task.completed;
        const dataTask = allTasks.find((item) => item.id === task.id);
        if (dataTask) {
          dataTask.completed = task.completed;
        }
        renderRoadmap();
        updateStats();
      });

      track.appendChild(taskNode);
    });

    board.appendChild(laneNode);
  });

  applyFilter(activeFilter);
  updateLaneSummary();
  persistRoadmap();
}

function updateLaneSummary() {
  document.querySelectorAll(".lane").forEach((laneElement) => {
    const laneKey = laneElement.dataset.laneKey;
    const laneDef = roadmap.find((lane) => lane.key === laneKey);
    if (!laneDef) return;

    const visibleTasks = laneDef.tasks.filter((task) => matchesFilter({ laneKey, scopeKey: scopeClass(task.scope).replace("scope-", "") }, activeFilter));
    const closed = visibleTasks.filter((task) => task.completed).length;
    const progress = laneElement.querySelector(".lane-progress");
    const total = laneElement.querySelector(".lane-total");

    if (progress && total) {
      progress.textContent = `${closed}/${visibleTasks.length} cerradas`;
      total.textContent = `${visibleTasks.length} tareas`;
    }
  });
}

function applyFilter(filter) {
  const lanes = Array.from(document.querySelectorAll(".lane"));
  lanes.forEach((laneElement) => {
    const cards = Array.from(laneElement.querySelectorAll(".task-card"));
    let visibleCount = 0;

    cards.forEach((card) => {
      const visible = filter === "all" || card.dataset.laneKey === filter || card.dataset.scopeKey === filter;
      card.classList.toggle("hidden-task", !visible);
      if (visible) visibleCount += 1;
    });

    laneElement.classList.toggle("hidden", visibleCount === 0);
  });
}

function setFilter(filter) {
  activeFilter = filter;
  chips.forEach((chip) => chip.classList.toggle("active", chip.dataset.filter === filter));
  applyFilter(filter);
  updateStats();
  updateLaneSummary();
}

chips.forEach((chip) => {
  chip.addEventListener("click", () => setFilter(chip.dataset.filter));
});

function startLivePulse() {
  document.body.classList.toggle("live-mode", autoAdvance.checked);
}

autoAdvance.addEventListener("change", startLivePulse);

renderRoadmap();
ensureScopeBars();
updateStats();
startLivePulse();
window.setInterval(updateStats, 1000);
