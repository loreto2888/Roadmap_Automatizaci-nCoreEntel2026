function renderPlanningPanel(visible) {
  const withWindows = visible.map((task) => ({ ...task, window: taskWindow(task) }));
  const starts = withWindows.map((task) => task.window.start.getTime());
  const ends = withWindows.map((task) => task.window.end.getTime());
  const now = new Date();

  const minTime = starts.length ? Math.min(...starts, now.getTime() - 7 * 86400000) : now.getTime() - 7 * 86400000;
  const maxTime = ends.length ? Math.max(...ends, now.getTime() + 7 * 86400000) : now.getTime() + 7 * 86400000;

  planningWindow.start = new Date(minTime);
  planningWindow.end = new Date(maxTime);
  planningWindow.totalMs = Math.max(1, maxTime - minTime);

  if (planningRange) {
    planningRange.textContent = `Rango: ${formatShortDate(planningWindow.start)} al ${formatShortDate(planningWindow.end)}`;
  }

  if (planningAxis) {
    planningAxis.innerHTML = "";
    const ticks = 8;
    for (let i = 0; i < ticks; i += 1) {
      const tick = document.createElement("div");
      tick.className = "planning-tick";
      const tickDate = new Date(minTime + (planningWindow.totalMs * i) / (ticks - 1));
      const label = document.createElement("span");
      label.textContent = formatShortDate(tickDate);
      tick.appendChild(label);
      planningAxis.appendChild(tick);
    }
  }

  if (planningRows) {
    planningRows.innerHTML = "";
    const candidates = withWindows
      .filter((task) => !task.completed)
      .sort((a, b) => priorityWeight(b.priority) - priorityWeight(a.priority))
      .slice(0, 8);

    candidates.forEach((task) => {
      const delay = taskDelayInfo(task);
      const left = ((task.window.start.getTime() - minTime) / planningWindow.totalMs) * 100;
      const width = ((task.window.end.getTime() - task.window.start.getTime()) / planningWindow.totalMs) * 100;

      const row = document.createElement("article");
      row.className = `planning-row ${delay.state}`;

      const head = document.createElement("div");
      head.className = "planning-row-head";
      const title = document.createElement("strong");
      title.textContent = `${task.id} - ${task.title}`;
      const meta = document.createElement("span");
      meta.textContent = `${task.priority} · ${task.scope} · ${delay.label}`;
      head.append(title, meta);

      const track = document.createElement("div");
      track.className = "planning-track";
      const bar = document.createElement("div");
      bar.className = `planning-bar ${scopeClass(task.scope)}`;
      bar.style.left = `${Math.max(0, Math.min(95, left))}%`;
      bar.style.width = `${Math.max(4, Math.min(100 - left, width))}%`;

      track.appendChild(bar);
      row.append(head, track);
      planningRows.appendChild(row);
    });

    if (candidates.length === 0) {
      const empty = document.createElement("article");
      empty.className = "planning-row";
      empty.textContent = "No hay tareas abiertas para mostrar en este filtro.";
      planningRows.appendChild(empty);
    }
  }

  if (urgentList) {
    urgentList.innerHTML = "";
    const alerts = withWindows
      .filter((task) => !task.completed && ["urgent", "important"].some((tag) => String(task.priority).toLowerCase().includes(tag)))
      .sort((a, b) => priorityWeight(b.priority) - priorityWeight(a.priority));

    const currentAlertIds = new Set(alerts.map((task) => task.id));
    if (notificationsInitialized) {
      alerts.forEach((task) => {
        if (!knownAlertIds.has(task.id)) {
          const toneType = String(task.priority).toLowerCase().includes("urgent") ? "urgent" : "important";
          pushToast(task, toneType);
          playAlertTone();
        }
      });
    }
    knownAlertIds = currentAlertIds;
    notificationsInitialized = true;

    alerts.slice(0, 6).forEach((task) => {
      const item = document.createElement("li");
      const type = String(task.priority).toLowerCase().includes("urgent") ? "urgent" : "important";
      item.className = `planning-alert-item ${type}`;
      const title = document.createElement("strong");
      title.textContent = `${task.id} - ${task.title}`;
      const meta = document.createElement("span");
      meta.textContent = `${task.priority} · ${task.owner}`;
      const actions = document.createElement("div");
      actions.className = "planning-alert-actions";
      const share = document.createElement("button");
      share.type = "button";
      share.textContent = "Compartir en Teams";
      share.addEventListener("click", () => {
        window.open(teamsShareUrl(task), "_blank", "noopener,noreferrer");
      });
      actions.appendChild(share);
      item.append(title, meta, actions);
      urgentList.appendChild(item);
    });

    if (!alerts.length) {
      const item = document.createElement("li");
      item.className = "planning-alert-item";
      item.textContent = "Sin alertas urgentes o importantes abiertas en esta vista.";
      urgentList.appendChild(item);
    }
  }

  updateNowMarker();
}

