function updateStats() {
  const visible = getVisibleTasks();
  const allTasks = getAllTasks();
  const closed = visible.filter((task) => task.completed).length;
  const total = visible.length || 1;
  const percent = Math.round((closed / total) * 100);
  const globalRemaining = allTasks.filter((task) => !task.completed).length;

  if (overallPercent) overallPercent.textContent = `${percent}%`;
  if (closedCount) closedCount.textContent = `${closed}/${visible.length}`;
  if (remainingCount) remainingCount.textContent = `${globalRemaining}`;
  if (timelineLabel) timelineLabel.textContent = `${percent}% completado`;
  if (lastUpdated) lastUpdated.textContent = formatClock(new Date());

  const active = visible.filter((task) => !task.completed && task.deposit !== "Cierre").length;
  const inProgress = visible.filter((task) => task.status === "En curso").length;
  const closing = visible.filter((task) => task.deposit === "Cierre" || task.completed).length;
  const overdueTasks = visible.filter((task) => taskDelayInfo(task).state === "overdue" && !task.completed);
  const overdue = overdueTasks.length;

  if (activeCount) activeCount.textContent = String(active);
  if (inProgressCount) inProgressCount.textContent = String(inProgress);
  if (closingCount) closingCount.textContent = String(closing);
  if (riskCount) {
    riskCount.textContent = String(overdue);
  }

  if (riskOwner) {
    if (!overdueTasks.length) {
      riskOwner.textContent = "Sin atrasos";
    } else {
      const scopeMap = overdueTasks.reduce((acc, task) => {
        const scopeKey = scopeClass(task.scope);
        const label =
          scopeKey === "scope-entel"
            ? "Entel"
            : scopeKey === "scope-intellicore"
              ? "Intellicore"
              : scopeKey === "scope-gestion"
                ? "Gestion"
                : scopeKey === "scope-splunk"
                  ? "Splunk"
                  : "Conjunta";
        acc[label] = (acc[label] || 0) + 1;
        return acc;
      }, {});

      const ranking = Object.entries(scopeMap).sort((a, b) => b[1] - a[1]);
      const [topScope, count] = ranking[0];
      if (ranking.length === 1) {
        riskOwner.textContent = `Frente: ${topScope} (${count})`;
      } else {
        riskOwner.textContent = `Principal: ${topScope} (${count}) +${ranking.length - 1}`;
      }
    }
  }

  updateDateAndCounter();

  timelineFill.style.height = `${percent}%`;
  timelineNode.style.setProperty("--node-y", `${Math.max(0, Math.min(500, 520 - (percent / 100) * 520))}px`);
  updateCharts(visible, percent);
  drawProgressChart();

  const key = planningKey(visible);
  if (key !== planningSignature) {
    renderPlanningPanel(visible);
    planningSignature = key;
  } else {
    updateNowMarker();
  }
}

function drawProgressChart() {
  if (!progressChart) return;
  const ctx = progressChart.getContext("2d");
  const allTasks = getAllTasks();
  
  // Agrupar por scope
  const scopeColors = {
    "Entel": "#67a9df",
    "Intellicore": "#e4580d",
    "Gestion": "#d96fd3",
    "Conjunta": "#ccb96a",
    "Splunk": "#9dc77b"
  };
  
  const scopeTotals = {
    "Entel": { total: 0, completed: 0 },
    "Intellicore": { total: 0, completed: 0 },
    "Gestion": { total: 0, completed: 0 },
    "Conjunta": { total: 0, completed: 0 },
    "Splunk": { total: 0, completed: 0 }
  };
  
  allTasks.forEach((task) => {
    const scope = task.scope || "Entel";
    if (scopeTotals[scope]) {
      scopeTotals[scope].total++;
      if (task.completed) scopeTotals[scope].completed++;
    }
  });
  
  const totalTasks = allTasks.length || 1;
  const completedTasks = allTasks.filter((t) => t.completed).length;
  const percent = Math.round((completedTasks / totalTasks) * 100);
  
  if (chartPercent) {
    chartPercent.textContent = `${percent}%`;
  }

  const centerX = progressChart.width / 2;
  const centerY = progressChart.height / 2;
  const radius = 85;
  const lineWidth = 18;

  ctx.clearRect(0, 0, progressChart.width, progressChart.height);

  // Dibujar segmentos por scope (pendientes + completadas)
  let currentAngle = -Math.PI / 2;
  
  Object.entries(scopeTotals).forEach(([scope, data]) => {
    const scopePercent = (data.total / totalTasks) * (Math.PI * 2);
    const color = scopeColors[scope];
    
    if (data.total > 0) {
      // Segmento de pendientes (más oscuro)
      const pendingPercent = ((data.total - data.completed) / totalTasks) * (Math.PI * 2);
      if (pendingPercent > 0.05) {
        ctx.beginPath();
        ctx.arc(centerX, centerY, radius, currentAngle, currentAngle + pendingPercent);
        ctx.lineWidth = lineWidth;
        ctx.strokeStyle = color;
        ctx.globalAlpha = 0.4;
        ctx.lineCap = "round";
        ctx.stroke();
        ctx.globalAlpha = 1;
      }
      currentAngle += pendingPercent;
      
      // Segmento de completadas (full color)
      const completedPercent = (data.completed / totalTasks) * (Math.PI * 2);
      if (completedPercent > 0.05) {
        ctx.beginPath();
        ctx.arc(centerX, centerY, radius, currentAngle, currentAngle + completedPercent);
        ctx.lineWidth = lineWidth;
        ctx.strokeStyle = color;
        ctx.globalAlpha = 1;
        ctx.lineCap = "round";
        ctx.stroke();
      }
      currentAngle += completedPercent;
    }
  });
  
  // Renderizar desglose
  updateBreakdown(scopeTotals);
}

