function ensureScopeBars() {
  if (!scopeBars || scopeBars.children.length > 0) return;

  scopeLegend.forEach((scope) => {
    const row = document.createElement("div");
    row.className = "scope-bar";
    row.dataset.scopeKey = scope.key;

    const head = document.createElement("div");
    head.className = "scope-bar-head";

    const name = document.createElement("span");
    name.textContent = scope.label;

    const value = document.createElement("strong");
    value.className = "scope-bar-value";
    value.textContent = "0";

    head.append(name, value);

    const track = document.createElement("div");
    track.className = "scope-bar-track";

    const fill = document.createElement("div");
    fill.className = `scope-bar-fill scope-${scope.key}`;
    track.appendChild(fill);

    row.append(head, track);
    scopeBars.appendChild(row);
  });
}

